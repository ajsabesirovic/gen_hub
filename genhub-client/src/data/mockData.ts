export function getReviewsForBabysitter(babysitterId: number): Review[] {
  return mockReviews.filter(review => review.babysitterId === babysitterId);
}

export function getAverageRating(babysitterId: number): number {
  const reviews = getReviewsForBabysitter(babysitterId);
  if (reviews.length === 0) return 0;
  const sum = reviews.reduce((acc, review) => acc + review.rating, 0);
  return sum / reviews.length;
}
